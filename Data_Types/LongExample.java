public class LongExample {
    public static void main(String[] args) {
        long distanceToMoon = 384400000L; // Distance to the moon in meters
        long speedOfLight = 299792458L;   // Speed of light in m/s
        long travelTime = distanceToMoon / speedOfLight;
        System.out.println("Travel Time to the Moon: " + travelTime + " seconds");
    }
}
