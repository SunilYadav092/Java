public class BooleanExample {
    public static void main(String[] args) {
        boolean bool1 = true;
        boolean bool2 = false;
        boolean result1 = bool1 == bool2;  // Boolean comparison
        boolean result2 = bool1 != bool2;  // Boolean negation comparison
        System.out.println("Result1: " + result1);
        System.out.println("Result2: " + result2);
    }
}
